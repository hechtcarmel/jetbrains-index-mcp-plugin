// ── Helper functions ────────────────────────────────────────────────────────

private val CSHARP_EXTENSIONS = setOf("cs", "csx")
private val FSHARP_EXTENSIONS = setOf("fs", "fsi", "fsx")
private val CSHARP_LANGUAGE_IDS = setOf("C#", "CSHARP", "CSharp")
private val FSHARP_LANGUAGE_IDS = setOf("F#", "FSHARP", "FSharp")

private fun isDotNetElement(element: PsiElement, supportedExtensions: Set<String>, supportedLanguageIds: Set<String>): Boolean {
    val langId = element.language.id
    if (supportedLanguageIds.any { it.equals(langId, ignoreCase = true) }) return true
    val ext = element.containingFile?.virtualFile?.extension?.lowercase()
    return ext in supportedExtensions
}

private fun createSourcePosition(project: Project, element: PsiElement): Any? {
    val file = element.containingFile
    val vf = file?.virtualFile
    val filePath = vf?.let { ProjectUtils.getToolFilePath(project, it) } ?: vf?.path ?: ""
    val document = file?.let { PsiDocumentManager.getInstance(project).getDocument(it) }
    val offset = element.textOffset
    val line = document?.getLineNumber(offset)?.plus(1) ?: 1
    val column = document?.let { offset - it.getLineStartOffset(it.getLineNumber(offset)) + 1 } ?: 1
    return RdProtocolBridge.createStruct("$MODEL_PKG.RdSourcePosition", filePath, line, column)
}

private fun rdSymbolToTypeElementData(symbol: Any, language: String): TypeElementData {
    return TypeElementData(
        name = RdProtocolBridge.getProperty(symbol, "name") as? String ?: "",
        qualifiedName = RdProtocolBridge.getProperty(symbol, "qualifiedName") as? String,
        file = RdProtocolBridge.getProperty(symbol, "filePath") as? String,
        line = RdProtocolBridge.getProperty(symbol, "line") as? Int,
        kind = RdProtocolBridge.getProperty(symbol, "kind") as? String ?: "UNKNOWN",
        language = language
    )
}

private fun rdSymbolToImplementationData(symbol: Any, language: String): ImplementationData? {
    return ImplementationData(
        name = RdProtocolBridge.getProperty(symbol, "name") as? String ?: "",
        file = RdProtocolBridge.getProperty(symbol, "filePath") as? String ?: return null,
        line = RdProtocolBridge.getProperty(symbol, "line") as? Int ?: return null,
        column = RdProtocolBridge.getProperty(symbol, "column") as? Int ?: 1,
        kind = (RdProtocolBridge.getProperty(symbol, "kind") as? String ?: "unknown").lowercase(),
        language = language
    )
}

private fun rdSymbolToCallElementData(symbol: Any, language: String): CallElementData? {
    return CallElementData(
        name = RdProtocolBridge.getProperty(symbol, "name") as? String ?: "",
        file = RdProtocolBridge.getProperty(symbol, "filePath") as? String ?: return null,
        line = RdProtocolBridge.getProperty(symbol, "line") as? Int ?: return null,
        column = RdProtocolBridge.getProperty(symbol, "column") as? Int ?: 1,
        language = language
    )
}

private fun rdStructureKind(kind: String): StructureKind = when (kind.uppercase()) {
    "CLASS", "ABSTRACT_CLASS" -> StructureKind.CLASS
    "INTERFACE" -> StructureKind.INTERFACE
    "STRUCT", "RECORD" -> StructureKind.RECORD
    "ENUM" -> StructureKind.ENUM
    "METHOD" -> StructureKind.METHOD
    "CONSTRUCTOR" -> StructureKind.CONSTRUCTOR
    "PROPERTY" -> StructureKind.PROPERTY
    "FIELD" -> StructureKind.FIELD
    "EVENT" -> StructureKind.PROPERTY
    "DELEGATE" -> StructureKind.CLASS
    "NAMESPACE" -> StructureKind.NAMESPACE
    else -> StructureKind.UNKNOWN
}

/**
 * Rebuilds a tree from a flat depth-annotated list of structure nodes.
 * Uses a two-pass approach since StructureNode is an immutable data class.
 */
private fun rebuildStructureTree(flatNodes: List<Any>): List<StructureNode> {
    if (flatNodes.isEmpty()) return emptyList()

    // First pass: extract flat data
    data class FlatEntry(
        val name: String,
        val kind: String,
        val modifiers: List<String>,
        val signature: String?,
        val line: Int,
        val depth: Int,
        val children: MutableList<FlatEntry> = mutableListOf()
    )

    val entries = flatNodes.map { flat ->
        @Suppress("UNCHECKED_CAST")
        FlatEntry(
            name = RdProtocolBridge.getProperty(flat, "name") as? String ?: "",
            kind = RdProtocolBridge.getProperty(flat, "kind") as? String ?: "UNKNOWN",
            modifiers = (RdProtocolBridge.getProperty(flat, "modifiers") as? List<String>) ?: emptyList(),
            signature = RdProtocolBridge.getProperty(flat, "signature") as? String,
            line = RdProtocolBridge.getProperty(flat, "line") as? Int ?: 1,
            depth = RdProtocolBridge.getProperty(flat, "depth") as? Int ?: 0
        )
    }

    // Second pass: build parent-child relationships
    val stack = mutableListOf<FlatEntry>()
    val roots = mutableListOf<FlatEntry>()

    for (entry in entries) {
        while (stack.isNotEmpty() && stack.last().depth >= entry.depth) {
            stack.removeLast()
        }
        if (stack.isEmpty()) {
            roots.add(entry)
        } else {
            stack.last().children.add(entry)
        }
        stack.add(entry)
    }

    // Third pass: convert to immutable StructureNode tree
    fun toStructureNode(entry: FlatEntry): StructureNode = StructureNode(
        name = entry.name,
        kind = rdStructureKind(entry.kind),
        modifiers = entry.modifiers,
        signature = entry.signature,
        line = entry.line,
        children = entry.children.map { toStructureNode(it) }
    )

    return roots.map { toStructureNode(it) }
}

// ── Abstract base for protocol-based handlers ───────────────────────────────

abstract class BaseRiderHandler<T>(
    final override val languageId: String,
    protected val displayLanguage: String,
    private val supportedExtensions: Set<String>,
    private val supportedLanguageIds: Set<String>,
) : LanguageHandler<T> {

    override fun isAvailable(): Boolean {
        return try {
            Class.forName("com.jetbrains.rider.projectView.SolutionLifecycleHost")
            true
        } catch (_: ClassNotFoundException) {
            try {
                Class.forName("com.jetbrains.rider.projectView.solution.SolutionLifecycleHost")
                true
            } catch (_: ClassNotFoundException) {
                false
            }
        }
    }

    override fun canHandle(element: PsiElement): Boolean =
        isAvailable() && isDotNetElement(element, supportedExtensions, supportedLanguageIds)
}

// ── Type Hierarchy Handlers ─────────────────────────────────────────────────

abstract class RiderTypeHierarchyHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<TypeHierarchyData>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    TypeHierarchyHandler {

    override fun getTypeHierarchy(element: PsiElement, project: Project, scope: BuiltInSearchScope): TypeHierarchyData? {
        val model = RdProtocolBridge.getModel(project) ?: return null
        val position = createSourcePosition(project, element) ?: return null
        val request = RdProtocolBridge.createStruct("$MODEL_PKG.RdTypeHierarchyRequest", position, scope.wireValue) ?: return null
        val result = RdProtocolBridge.invokeCall(model, "getTypeHierarchy", request) ?: return null

        val elementInfo = RdProtocolBridge.getProperty(result, "element") ?: return null
        @Suppress("UNCHECKED_CAST")
        val supertypes = (RdProtocolBridge.getProperty(result, "supertypes") as? List<Any>) ?: emptyList()
        @Suppress("UNCHECKED_CAST")
        val subtypes = (RdProtocolBridge.getProperty(result, "subtypes") as? List<Any>) ?: emptyList()

        return TypeHierarchyData(
            element = rdSymbolToTypeElementData(elementInfo, displayLanguage),
            supertypes = supertypes.map { rdSymbolToTypeElementData(it, displayLanguage) },
            subtypes = subtypes.map { rdSymbolToTypeElementData(it, displayLanguage) }
        )
    }
}

// ── Implementations Handlers ────────────────────────────────────────────────

abstract class RiderImplementationsHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<List<ImplementationData>>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    ImplementationsHandler {

    override fun findImplementations(element: PsiElement, project: Project, scope: BuiltInSearchScope): List<ImplementationData>? {
        val model = RdProtocolBridge.getModel(project) ?: return null
        val position = createSourcePosition(project, element) ?: return null
        val request = RdProtocolBridge.createStruct("$MODEL_PKG.RdImplementationsRequest", position, scope.wireValue) ?: return null
        val result = RdProtocolBridge.invokeCall(model, "findImplementations", request) ?: return null

        @Suppress("UNCHECKED_CAST")
        val implementations = (RdProtocolBridge.getProperty(result, "implementations") as? List<Any>) ?: emptyList()
        val directResults = implementations.mapNotNull { rdSymbolToImplementationData(it, displayLanguage) }
        if (directResults.isNotEmpty()) return directResults

        val hierarchyRequest = RdProtocolBridge.createStruct("$MODEL_PKG.RdTypeHierarchyRequest", position, scope.wireValue) ?: return directResults
        val hierarchyResult = RdProtocolBridge.invokeCall(model, "getTypeHierarchy", hierarchyRequest) ?: return directResults
        @Suppress("UNCHECKED_CAST")
        val subtypes = (RdProtocolBridge.getProperty(hierarchyResult, "subtypes") as? List<Any>) ?: emptyList()
        val subtypeResults = subtypes.mapNotNull { rdSymbolToImplementationData(it, displayLanguage) }
        if (subtypeResults.isNotEmpty()) return subtypeResults

        val root = RdProtocolBridge.getProperty(hierarchyResult, "element")
        val typeName = root?.let { RdProtocolBridge.getProperty(it, "name") as? String }
            ?: DotNetTextSearchSupport.wordAt(element)
            ?: return subtypeResults
        return DotNetTextSearchSupport.findImplementations(project, typeName, scope, displayLanguage, 200)
    }
}

// ── Call Hierarchy Handlers ─────────────────────────────────────────────────

abstract class RiderCallHierarchyHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<CallHierarchyData>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    CallHierarchyHandler {

    override fun getCallHierarchy(
        element: PsiElement,
        project: Project,
        direction: String,
        depth: Int,
        scope: BuiltInSearchScope
    ): CallHierarchyData? {
        val fallback = {
            DotNetTextSearchSupport.findCallHierarchy(project, element, direction, scope, displayLanguage, 200)
        }
        val model = RdProtocolBridge.getModel(project) ?: return fallback()
        val position = createSourcePosition(project, element) ?: return fallback()
        val request = RdProtocolBridge.createStruct("$MODEL_PKG.RdCallHierarchyRequest", position, direction, depth, scope.wireValue)
            ?: return fallback()
        val result = RdProtocolBridge.invokeCall(model, "getCallHierarchy", request) ?: return fallback()

        val root = RdProtocolBridge.getProperty(result, "root") ?: return null
        @Suppress("UNCHECKED_CAST")
        val calls = (RdProtocolBridge.getProperty(result, "calls") as? List<Any>) ?: emptyList()
        val convertedCalls = calls.mapNotNull { rdSymbolToCallElementData(it, displayLanguage) }
        if (convertedCalls.isEmpty()) return fallback()

        return CallHierarchyData(
            element = rdSymbolToCallElementData(root, displayLanguage) ?: return null,
            calls = convertedCalls
        )
    }
}

// ── Super Methods Handlers ──────────────────────────────────────────────────

abstract class RiderSuperMethodsHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<SuperMethodsData>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    SuperMethodsHandler {

    override fun findSuperMethods(element: PsiElement, project: Project): SuperMethodsData? {
        val model = RdProtocolBridge.getModel(project) ?: return null
        val position = createSourcePosition(project, element) ?: return null
        val request = RdProtocolBridge.createStruct("$MODEL_PKG.RdSuperMethodsRequest", position) ?: return null
        val result = RdProtocolBridge.invokeCall(model, "findSuperMethods", request) ?: return null

        val method = RdProtocolBridge.getProperty(result, "method") ?: return null
        @Suppress("UNCHECKED_CAST")
        val hierarchy = (RdProtocolBridge.getProperty(result, "hierarchy") as? List<Any>) ?: emptyList()

        val fallbackMethodName = DotNetTextSearchSupport.methodNameAt(project, element).orEmpty()
        fun usableName(value: String?): String =
            value?.takeUnless { it.isBlank() || it.equals("unknown", ignoreCase = true) } ?: fallbackMethodName
        val methodName = usableName(RdProtocolBridge.getProperty(method, "name") as? String)

        return SuperMethodsData(
            method = MethodData(
                name = methodName,
                signature = (RdProtocolBridge.getProperty(method, "signature") as? String)
                    ?.takeUnless { it.isBlank() || it.equals("unknown", ignoreCase = true) } ?: methodName,
                containingClass = (RdProtocolBridge.getProperty(method, "qualifiedName") as? String)
                    ?.substringBeforeLast('.', "unknown") ?: "unknown",
                file = RdProtocolBridge.getProperty(method, "filePath") as? String ?: return null,
                line = RdProtocolBridge.getProperty(method, "line") as? Int ?: return null,
                column = RdProtocolBridge.getProperty(method, "column") as? Int ?: 1,
                language = displayLanguage
            ),
            hierarchy = hierarchy.map { superInfo ->
                val symbol = RdProtocolBridge.getProperty(superInfo, "symbol")
                val superName = usableName(symbol?.let { RdProtocolBridge.getProperty(it, "name") as? String })
                SuperMethodData(
                    name = superName,
                    signature = (symbol?.let { RdProtocolBridge.getProperty(it, "signature") as? String })
                        ?.takeUnless { it.isBlank() || it.equals("unknown", ignoreCase = true) } ?: superName,
                    containingClass = RdProtocolBridge.getProperty(superInfo, "containingTypeName") as? String ?: "",
                    containingClassKind = RdProtocolBridge.getProperty(superInfo, "containingTypeKind") as? String ?: "CLASS",
                    file = symbol?.let { RdProtocolBridge.getProperty(it, "filePath") as? String },
                    line = symbol?.let { RdProtocolBridge.getProperty(it, "line") as? Int },
                    column = symbol?.let { RdProtocolBridge.getProperty(it, "column") as? Int },
                    isInterface = RdProtocolBridge.getProperty(superInfo, "isInterface") as? Boolean ?: false,
                    depth = RdProtocolBridge.getProperty(superInfo, "depth") as? Int ?: 0,
                    language = displayLanguage
                )
            }
        )
    }
}

// ── Structure Handlers ──────────────────────────────────────────────────────

abstract class RiderStructureHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<List<StructureNode>>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    StructureHandler {

    override fun getFileStructure(file: PsiFile, project: Project): List<StructureNode> {
        val model = RdProtocolBridge.getModel(project) ?: return emptyList()
        val filePath = file.virtualFile?.let { ProjectUtils.getToolFilePath(project, it) }
            ?: file.virtualFile?.path ?: return emptyList()
        val request = RdProtocolBridge.createStruct("$MODEL_PKG.RdFileStructureRequest", filePath) ?: return emptyList()
        val result = RdProtocolBridge.invokeCall(model, "getFileStructure", request) ?: return emptyList()

        @Suppress("UNCHECKED_CAST")
        val nodes = (RdProtocolBridge.getProperty(result, "nodes") as? List<Any>) ?: emptyList()
        return rebuildStructureTree(nodes)
    }
}

// ── Symbol Reference Handlers ────────────────────────────────────────────────

abstract class RiderSymbolReferenceHandler(
    languageId: String,
    displayLanguage: String,
    supportedExtensions: Set<String>,
    supportedLanguageIds: Set<String>,
) : BaseRiderHandler<PsiNamedElement>(languageId, displayLanguage, supportedExtensions, supportedLanguageIds),
    SymbolReferenceHandler {

    override val languageName: String = displayLanguage

    override fun resolveSymbol(project: Project, symbol: String): Result<PsiNamedElement> {
        val typeName = symbol.substringBefore('#').substringBefore('(')
        val element = ClassResolver.findClassByName(project, typeName) as? PsiNamedElement
            ?: return Result.failure(IllegalArgumentException("Could not resolve $displayLanguage symbol: $symbol"))
        return Result.success(element)
    }
}

